let x = 123.00;      // 실수
console.log(`x : ${x}`);
let y = 123;         // 정수
console.log(`y : ${y}`);
let z1 = 123e3;      // 123000
console.log(`z1 : ${z1}`);
let z2 = 123e-3;     // 0.123
console.log(`z2 : ${z2}`);

let x1 = (20 > 10); // x는 true가 된다. 
console.log(`x1 : ${x1}`);
let y1 = (10 > 20); // y는 false가 된다. 
console.log(`y1 : ${y1}`);

let s1 = "abc"; 	// s는 문자열 "abc"를 저장한다.
console.log(`s1 : ${s1}`);
console.log(`s1.length : ${s1.length}`);
let s2 = 'abc'; 	// OK!
console.log(`s2 : ${s2}`);
console.log(`s2.length : ${s2.length}`);
let s3 = "그는 '슈퍼맨'이라고 불린다"; 	// 문자열 안에 작은 따옴표를 사용할 수 있다. 
console.log(`s3 : ${s3}`);
console.log(`s3.length : ${s3.length}`);